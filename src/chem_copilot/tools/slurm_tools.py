"""LangGraph tools for Slurm job management."""

from __future__ import annotations

from typing import Optional

from langchain_core.tools import tool
from pydantic import BaseModel, Field

from chem_copilot.parsers.output_parser import OutputParser
from chem_copilot.slurm.manager import SlurmManager


def _manager() -> SlurmManager:
    """Lazy singleton — config read from env at call time."""
    return SlurmManager()


# ------------------------------------------------------------------
# Submit
# ------------------------------------------------------------------

class SubmitFHIAimsInput(BaseModel):
    """Input schema for FHI-aims job submission."""
    control_in: str = Field(description="Contents of control.in (without species defaults)")
    geometry_in: str = Field(description="Contents of geometry.in")
    job_name: str = Field(default="aims", description="Slurm job name")
    basis_tier: str = Field(default="light", description="Basis tier for species defaults")
    partition: Optional[str] = Field(default=None, description="Slurm partition override")
    time_limit: Optional[str] = Field(default=None, description="Time limit, e.g. '4:00:00'")
    ntasks: Optional[int] = Field(default=None, description="Number of MPI tasks")
    cpus_per_task: Optional[int] = Field(default=None, description="CPUs per task")


@tool("submit_fhiaims_job", args_schema=SubmitFHIAimsInput)
def submit_fhiaims_job(
    control_in: str,
    geometry_in: str,
    job_name: str = "aims",
    basis_tier: str = "light",
    partition: Optional[str] = None,
    time_limit: Optional[str] = None,
    ntasks: Optional[int] = None,
    cpus_per_task: Optional[int] = None,
) -> str:
    """Submit an FHI-aims calculation to the Slurm cluster.

    Takes control.in and geometry.in contents (as generated by
    generate_fhiaims_input), writes them to a job directory,
    appends the appropriate species defaults, and submits via sbatch.

    Returns the Slurm job ID and job directory path.

    Use this when the user wants to actually run an FHI-aims calculation.
    """
    mgr = _manager()
    overrides: dict[str, str] = {}
    if partition:
        overrides["partition"] = partition
    if time_limit:
        overrides["time"] = time_limit
    if ntasks is not None:
        overrides["ntasks"] = str(ntasks)
    if cpus_per_task is not None:
        overrides["cpus_per_task"] = str(cpus_per_task)

    try:
        job_id = mgr.submit_fhiaims(
            control_in=control_in,
            geometry_in=geometry_in,
            job_name=job_name,
            basis_tier=basis_tier,
            slurm_overrides=overrides or None,
        )
        job_dir = mgr._find_job_dir(job_id)
        return f"Job submitted successfully.\nJob ID: {job_id}\nJob directory: {job_dir}"
    except Exception as e:
        return f"Job submission failed: {e}"


