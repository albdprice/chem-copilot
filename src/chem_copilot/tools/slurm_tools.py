class ListJobsInput(BaseModel):
    """Input schema for listing jobs."""
    n: int = Field(default=10, description="Number of recent jobs to list")


@tool("list_recent_jobs", args_schema=ListJobsInput)
def list_recent_jobs(n: int = 10) -> str:
    """List recent jobs submitted through chem-copilot.

    Shows job ID, state, program, and directory for each job.
    """
    mgr = _manager()
    jobs = mgr.list_recent_jobs(n=n)

    if not jobs:
        return "No recent jobs found."

    lines = []
    for j in jobs:
        state_icon = {"COMPLETED": "+", "RUNNING": ">", "PENDING": "~", "FAILED": "X"}.get(
            j.state, "?"
        )
        lines.append(f"[{state_icon}] {j.job_id}  {j.state:<12s}  {j.name}  {j.job_dir}")

    return "\n".join(lines)


