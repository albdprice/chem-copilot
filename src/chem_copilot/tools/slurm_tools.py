class GetResultInput(BaseModel):
    """Input schema for result retrieval."""
    job_id: str = Field(description="Slurm job ID to get results for")


@tool("get_job_result", args_schema=GetResultInput)
def get_job_result(job_id: str) -> str:
    """Retrieve and parse the results of a completed calculation.

    Finds the output file for the given job ID, parses it using
    the appropriate parser (FHI-aims, Psi4, Gaussian), and returns
    a structured summary of the results.

    Use this after a job has COMPLETED to analyze the results.
    """
    mgr = _manager()

    # Check status first
    info = mgr.get_job_status(job_id)
    if info.state in ("PENDING", "RUNNING"):
        return f"Job {job_id} is still {info.state}. Wait for it to complete before retrieving results."

    output_path = mgr.get_output_path(job_id)
    if output_path is None:
        return f"No output file found for job {job_id}. Job state: {info.state}"

    program = mgr.get_program(job_id)
    parser = OutputParser()

    try:
        result = parser.parse_file(output_path, program=program)
    except Exception as e:
        return f"Failed to parse output for job {job_id}: {e}\nOutput file: {output_path}"

    lines = [
        f"Job {job_id} — {program} — {result.calc_type}",
        f"Status: {'CONVERGED' if result.success else 'FAILED/INCOMPLETE'}",
    ]

    e = result.energies
    if e.total_energy is not None:
        lines.append(f"Total energy: {e.total_energy:.8f} eV ({e.total_energy_hartree:.10f} Ha)")
    if e.scf_energies:
        lines.append(f"SCF iterations: {len(e.scf_energies)}")

    orb = result.orbitals
    if orb.homo_lumo_gap is not None:
        lines.append(f"HOMO-LUMO gap: {orb.homo_lumo_gap:.4f} eV")

    if result.forces.max_force is not None:
        lines.append(f"Max force: {result.forces.max_force:.6f} eV/Ang")

    opt = result.optimization
    if opt.n_steps > 1:
        lines.append(f"Optimization: {opt.n_steps} steps, converged={opt.converged}")

    if result.n_atoms:
        lines.append(f"Atoms: {result.n_atoms}")
    if result.n_basis_functions:
        lines.append(f"Basis functions: {result.n_basis_functions}")

    if result.warnings:
        lines.append("Warnings: " + "; ".join(result.warnings))

    lines.append(f"Output file: {output_path}")

    return "\n".join(lines)
