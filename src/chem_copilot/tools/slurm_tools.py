class CheckJobInput(BaseModel):
    """Input schema for job status check."""
    job_id: str = Field(description="Slurm job ID to check")


@tool("check_job_status", args_schema=CheckJobInput)
def check_job_status(job_id: str) -> str:
    """Check the status of a Slurm job.

    Returns the job state (PENDING, RUNNING, COMPLETED, FAILED, etc.),
    node, elapsed time, and output file location.

    Use this to check if a submitted calculation has finished.
    """
    mgr = _manager()
    info = mgr.get_job_status(job_id)

    lines = [
        f"Job ID: {info.job_id}",
        f"State: {info.state}",
    ]
    if info.name:
        lines.append(f"Name: {info.name}")
    if info.node:
        lines.append(f"Node: {info.node}")
    if info.elapsed:
        lines.append(f"Elapsed: {info.elapsed}")
    if info.exit_code:
        lines.append(f"Exit code: {info.exit_code}")
    if info.job_dir:
        lines.append(f"Directory: {info.job_dir}")
    if info.output_file:
        lines.append(f"Output file: {info.output_file}")

    return "\n".join(lines)


